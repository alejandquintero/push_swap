#include "../inc/libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*aux;

	if (lst != NULL)
	{
		aux = lst;
		while (aux != NULL)
		{
			f(aux->content);
			aux = aux->next;
		}
	}
}
